def concat(str1: str, str2: str) -> str:
    """takes a string str1 and a string str2 as arguments
    and returns a concatenated string.

    Args:
        str1 (str): 1st str to concatenate
        str2 (str): 2nd str to concatenate

    Returns:
        str: concatenated str
    """
    return str1 + str2
