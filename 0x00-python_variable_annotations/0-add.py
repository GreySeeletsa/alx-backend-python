def add(a: float, b: float) -> float:
    """Type-annotated func takes float a and float b
    as arg and returns their sum as a float.

    Args:
        a (float): 1st float to add.
        b (float): 2nd float to add.

    Returns:
        float: Sum of 2 floats.
    """
    return a + b
